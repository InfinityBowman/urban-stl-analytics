export type PricePoint = { year: number; price: number; rent: number }

export function getNeighborhoodPriceForYear(
  id: string,
  year: number,
): PricePoint | null {
  const n = priceDataset.find((n) => n.id === id)
  if (!n) return null
  const p = n.history.find((h) => h.year === year)
  return p ?? null
}
